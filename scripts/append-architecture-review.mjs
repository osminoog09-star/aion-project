/**
 * Create architecture review request (Cursor escalation — no owner relay).
 *
 *   node scripts/append-architecture-review.mjs \
 *     --template gps-storage \
 *     --title "Headless GPS orphan sessions" \
 *     --subsystem "background-drive" \
 *     --reasoning "..." \
 *     --architecture-concern "..." \
 *     --proposed-direction "..." \
 *     --confidence experimental \
 *     [--runtime-concern "..."] [--scaling-concern "..."] [--dependency-concern "..."] \
 *     [--subsystems mobile-app,background-drive] [--commits abc123] \
 *     [--roadmap-item post-shift-analytics] [--dependency-nodes route-gps-store]
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const queuePath = path.join(root, "src/content/architecture-review-queue.json");
const feedPath = path.join(root, "src/content/ecosystem-implementation-feed.json");

function arg(name) {
  const i = process.argv.indexOf(name);
  if (i === -1 || !process.argv[i + 1]) return null;
  return process.argv[i + 1];
}

const title = arg("--title");
const reasoning = arg("--reasoning");
const architectureConcern = arg("--architecture-concern");
const proposedDirection = arg("--proposed-direction");
if (!title || !reasoning || !architectureConcern || !proposedDirection) {
  console.error(
    "Required: --title --reasoning --architecture-concern --proposed-direction",
  );
  process.exit(1);
}

const templateId = arg("--template") ?? "general";
const subsystem = arg("--subsystem") ?? "operations-center";
const confidence = arg("--confidence") ?? "experimental";
const subRaw = arg("--subsystems");
const subsystemIds = subRaw
  ? subRaw.split(",").map((s) => s.trim()).filter(Boolean)
  : [subsystem];

const queue = JSON.parse(fs.readFileSync(queuePath, "utf8"));
const template = queue.templates.find((t) => t.id === templateId);
const now = new Date().toISOString();
const id = `ar-${Date.now()}-${Math.random().toString(36).slice(2, 8)}`;

const requestBase = {
  id,
  createdAt: now,
  updatedAt: now,
  status: "pending",
  templateId,
  title,
  subsystem,
  subsystemIds,
  reasoning,
  architectureConcern,
  scalingConcern: arg("--scaling-concern") ?? undefined,
  runtimeConcern: arg("--runtime-concern") ?? undefined,
  dependencyConcern: arg("--dependency-concern") ?? undefined,
  proposedDirection,
  confidence,
  affectedSystems: subsystemIds,
  roadmapItemId: arg("--roadmap-item") ?? undefined,
  dependencyNodeIds: arg("--dependency-nodes")
    ? arg("--dependency-nodes").split(",").map((s) => s.trim())
    : undefined,
  linkedCommitHashes: arg("--commits")
    ? arg("--commits").split(",").map((s) => s.trim())
    : undefined,
  linkedFeedEventIds: [],
};

// Inline packet build (avoid TS import in mjs — duplicate minimal markdown)
const markdown = `# AION Architecture Review Packet

## Meta
- **Title:** ${title}
- **Confidence:** ${confidence}
- **Template:** ${templateId}

## Reasoning (Cursor)
${reasoning}

## Concerns
Architecture: ${architectureConcern}
${requestBase.runtimeConcern ? `Runtime: ${requestBase.runtimeConcern}` : ""}
${requestBase.scalingConcern ? `Scaling: ${requestBase.scalingConcern}` : ""}

## Proposed direction
${proposedDirection}

---
*Generated by append-architecture-review.mjs*
`;

const request = {
  ...requestBase,
  reviewPacket: {
    generatedAt: now,
    markdown,
    sections: {
      context: `Template: ${template?.title ?? templateId}`,
      concern: architectureConcern,
      constraints: "MASTER honesty rules apply.",
      proposedDirection,
      questionsForReviewer: [
        "Is proposed direction sound?",
        "Top runtime risks?",
        "Continue parallel execution?",
      ],
    },
  },
};

queue.requests = [request, ...queue.requests];
queue.lastUpdated = now.slice(0, 10);
fs.writeFileSync(queuePath, `${JSON.stringify(queue, null, 2)}\n`);

const feed = JSON.parse(fs.readFileSync(feedPath, "utf8"));
const feedId = `evt-${now.slice(0, 10)}-review-${Math.random().toString(36).slice(2, 8)}`;
feed.items = [
  {
    id: feedId,
    occurredAt: now.slice(0, 10),
    title: `Architecture review requested: ${title}`,
    summary: architectureConcern,
    subsystemIds,
    eventType: "architecture_review_requested",
    task: `architecture-review:${id}`,
    reasoning,
    confidence,
    repository: "aion-project",
    rollup: { fullyDone: ["✅ Review request queued"], partiallyDone: [], notStarted: [], technicalDebt: [] },
    stillMissing: [],
    blocked: [],
    impacts: { release: "low", otaApk: "none", backend: "low", realtime: "none", ux: "medium", cloud: "low" },
    validation: { web_build: "pending" },
  },
  ...feed.items,
];
feed.lastUpdated = now.slice(0, 10);
fs.writeFileSync(feedPath, `${JSON.stringify(feed, null, 2)}\n`);

console.log(JSON.stringify({ ok: true, id, feedId, packetPath: queuePath }, null, 2));
